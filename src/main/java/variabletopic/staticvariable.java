package variabletopic;

public class staticvariable  // static variable is defined as  we use static key word in variable
                            //  we use static keyword means no need  to creat an object we call directly
                                // no need to use object
{
      static String degree = " cse";
    static int volume = 50 ;

    public static void main(String[] args) {
        System.out.println(degree);
        System.out.println(volume);
    }

}
